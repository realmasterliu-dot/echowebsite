/**
 * [已弃用] 将品牌 woff2 编成 base64 进包。
 * 现已改走 Zeoseven CDN：npm run fonts:cdn
 * 保留本脚本仅作应急回退，勿在默认构建路径引用输出物。
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const fontsDir = path.resolve(__dirname, '../src/assets/fonts')
const outJs = path.join(fontsDir, 'jiangcheng-base64.js')
const outScssDir = path.resolve(__dirname, '../src/styles/generated')
const outScss = path.join(outScssDir, 'jiangcheng-faces.scss')

const files = [
  { key: 'FONT_300_WOFF2_BASE64', name: 'jiangcheng-300.woff2', weight: '300' },
  { key: 'FONT_400_WOFF2_BASE64', name: 'jiangcheng-400.woff2', weight: '400' },
  { key: 'FONT_500_WOFF2_BASE64', name: 'jiangcheng-500.woff2', weight: '500' },
  { key: 'FONT_600_WOFF2_BASE64', name: 'jiangcheng-600.woff2', weight: '600' },
  { key: 'FONT_700_WOFF2_BASE64', name: 'jiangcheng-700.woff2', weight: '700' },
]

const jsParts = [
  '/**',
  ' * AUTO-GENERATED by scripts/encode-brand-fonts.mjs — do not edit.',
  ' * Regenerate: npm run fonts:encode',
  ' */',
  '',
  "export const FONT_FAMILY = 'JiangChengRound'",
  '',
  'export const FONT_FACE_KEYS = [',
  ...files.map((f) => `  { key: '${f.key}', weight: '${f.weight}' },`),
  ']',
  '',
]

const scssParts = [
  '/* AUTO-GENERATED by scripts/encode-brand-fonts.mjs — do not edit. */',
  '/* mp-weixin: @font-face + base64 Data URL（无本地路径、无需 CDN） */',
  '',
]

for (const { key, name, weight } of files) {
  const filePath = path.join(fontsDir, name)
  if (!fs.existsSync(filePath)) {
    console.error('Missing font file:', filePath)
    process.exit(1)
  }
  const b64 = fs.readFileSync(filePath).toString('base64')
  jsParts.push(`export const ${key} = '${b64}'`, '')

  scssParts.push(`@font-face {`)
  scssParts.push(`  font-family: 'JiangChengRound';`)
  scssParts.push(
    `  src: url('data:font/woff2;charset=utf-8;base64,${b64}') format('woff2');`,
  )
  scssParts.push(`  font-weight: ${weight};`)
  scssParts.push(`  font-style: normal;`)
  scssParts.push(`}`)
  scssParts.push('')

  console.log(name, `${(b64.length / 1024).toFixed(1)} KB base64`)
}

fs.mkdirSync(outScssDir, { recursive: true })
fs.writeFileSync(outJs, jsParts.join('\n'), 'utf8')
fs.writeFileSync(outScss, scssParts.join('\n'), 'utf8')
console.log('Wrote', outJs, `(${(fs.statSync(outJs).size / 1024).toFixed(1)} KB)`)
console.log('Wrote', outScss, `(${(fs.statSync(outScss).size / 1024).toFixed(1)} KB)`)
