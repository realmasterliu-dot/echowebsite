/**
 * Encode brand woff2 fonts to base64 for mp-weixin loadFontFace Data URL.
 * Usage: npm run fonts:encode
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const fontsDir = path.resolve(__dirname, '../src/static/fonts')
const outFile = path.join(fontsDir, 'jiangcheng-base64.js')

const files = [
  { key: 'FONT_300_WOFF2_BASE64', name: 'jiangcheng-300.woff2', weight: '300' },
  { key: 'FONT_400_WOFF2_BASE64', name: 'jiangcheng-400.woff2', weight: '400' },
  { key: 'FONT_500_WOFF2_BASE64', name: 'jiangcheng-500.woff2', weight: '500' },
  { key: 'FONT_600_WOFF2_BASE64', name: 'jiangcheng-600.woff2', weight: '600' },
  { key: 'FONT_700_WOFF2_BASE64', name: 'jiangcheng-700.woff2', weight: '700' },
]

const parts = [
  '/**',
  ' * AUTO-GENERATED by scripts/encode-brand-fonts.mjs — do not edit by hand.',
  ' * Regenerate: npm run fonts:encode',
  ' * Used by services/fonts.js → uni.loadFontFace Data URL (no CDN).',
  ' * Weights: 300 / 400 / 500 / 600 / 700',
  ' */',
  '',
  "export const FONT_FAMILY = 'JiangChengRound'",
  '',
  '/** @type {{ key: string, weight: string }[]} */',
  'export const FONT_FACE_KEYS = [',
  ...files.map((f) => `  { key: '${f.key}', weight: '${f.weight}' },`),
  ']',
  '',
]

for (const { key, name } of files) {
  const filePath = path.join(fontsDir, name)
  if (!fs.existsSync(filePath)) {
    console.error('Missing font file:', filePath)
    process.exit(1)
  }
  const b64 = fs.readFileSync(filePath).toString('base64')
  parts.push(`export const ${key} = '${b64}'`)
  parts.push('')
  console.log(name, `${(b64.length / 1024).toFixed(1)} KB base64`)
}

fs.writeFileSync(outFile, parts.join('\n'), 'utf8')
console.log('Wrote', outFile, `(${(fs.statSync(outFile).size / 1024).toFixed(1)} KB)`)
