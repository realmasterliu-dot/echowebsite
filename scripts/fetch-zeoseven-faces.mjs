/**
 * 拉取 Zeoseven 江城圆体 CSS，把相对 woff2 改写为绝对 HTTPS，
 * 供 mp-weixin 的 wxss @font-face 使用（支持 unicode-range，不进包字体二进制）。
 *
 * Usage: npm run fonts:cdn
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const outDir = path.resolve(__dirname, '../src/styles/generated')
const outScss = path.join(outDir, 'jiangcheng-cdn-faces.scss')

/** @type {{ weight: string, path: string }[]} */
const WEIGHTS = [
  { weight: '300', path: '300w' },
  { weight: '400', path: '400w' },
  { weight: '500', path: 'main' },
  { weight: '600', path: '600w' },
  { weight: '700', path: '700w' },
]

const FAMILY = 'JiangChengYuanTi'
const BASE = 'https://fontsapi.zeoseven.com/59'

/**
 * @param {string} css
 * @param {string} cssUrl
 * @param {string} weight
 */
function rewriteFaces(css, cssUrl, weight) {
  const faces = []
  const re = /@font-face\s*\{([^}]+)\}/g
  let m
  while ((m = re.exec(css))) {
    const body = m[1]
    const srcMatch = body.match(/url\(\s*['"]?([^'")\s]+)/)
    if (!srcMatch) continue
    const abs = new URL(srcMatch[1], cssUrl).href
    const rangeMatch = body.match(/unicode-range\s*:\s*([^;}+]+)/i)
    const range = rangeMatch ? rangeMatch[1].trim().replace(/\s+/g, '') : null

    let block = `@font-face {\n  font-family: '${FAMILY}';\n  src: url('${abs}') format('woff2');\n  font-weight: ${weight};\n  font-style: normal;\n  font-display: swap;`
    if (range) block += `\n  unicode-range: ${range};`
    block += `\n}`
    faces.push(block)
  }
  return faces
}

async function main() {
  const parts = [
    '/* AUTO-GENERATED by scripts/fetch-zeoseven-faces.mjs — do not edit. */',
    '/* Regenerate: npm run fonts:cdn */',
    `/* Source: ${BASE}/{300w|400w|main|600w|700w}/result.css */`,
    '',
  ]

  let totalFaces = 0
  for (const { weight, path: p } of WEIGHTS) {
    const cssUrl = `${BASE}/${p}/result.css`
    process.stdout.write(`fetch ${cssUrl} ... `)
    const res = await fetch(cssUrl)
    if (!res.ok) throw new Error(`HTTP ${res.status} ${cssUrl}`)
    const css = await res.text()
    const faces = rewriteFaces(css, cssUrl, weight)
    totalFaces += faces.length
    console.log(`${faces.length} faces`)
    parts.push(`/* weight ${weight} — ${faces.length} faces */`, ...faces, '')
  }

  fs.mkdirSync(outDir, { recursive: true })
  const out = parts.join('\n')
  fs.writeFileSync(outScss, out, 'utf8')
  console.log(
    `Wrote ${outScss} (${(Buffer.byteLength(out) / 1024).toFixed(1)} KB, ${totalFaces} faces)`,
  )
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
